public class Task8 {
    public static void main(String[] args) {
        int a = 5; 
        int b = 3; 

        System.out.println("a & b: " + (a & b)); 
        System.out.println("a | b: " + (a | b));
        System.out.println("a ^ b: " + (a ^ b)); 
        System.out.println("~a: " + (~a));  
        System.out.println("a << 1: " + (a << 1)); 
        System.out.println("a >> 1: " + (a >> 1));

        boolean x = true;
        boolean y = false;

        System.out.println("x && y: " + (x && y));
        System.out.println("x || y: " + (x || y));
        System.out.println("!x: " + (!x));
    }
}
